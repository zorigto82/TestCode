public class Calculator {

    public static int sum(int value1, int value2, int value3) {
        return value1 + value2 + value3;
    }

    public static int multiply(int value1, int value2, int value3) {
        return value1 * value2 * value3;
    }

    public static int subtract(int value1, int value2, int value3) {
        return value1 - value2 - value3;
    }
}

